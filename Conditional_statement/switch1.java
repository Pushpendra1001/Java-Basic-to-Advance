class switch1 {
    public static void main(String[] args) {
        
        int number = 6;

        switch(number){
            case 1 : System.out.println("Burger");
            break;
            case 2 : System.out.println("pizza");
            break;
            case 3 : System.out.println("Badam shake ");
            break;
            default: System.out.println("Kuch nhi hai ");
        }
    }
}
